/*Print octal and hexadecimal value of a given number*/

#include<stdio.h>
int main(){
    int num=672;
    printf("The number is : %d\n", num);    //672
    printf("The octal value of the number is : %o\n", num); //1240
    printf("The hexadecimal value of the number is : %X\n", num);   //2A0
    return 0;
}
